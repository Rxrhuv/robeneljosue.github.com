            <h2>Humba</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>1 kg pork belly, cut into serving pieces</li>
                <li>1 cup pineapple juice</li>
                <li>1/2 cup soy sauce</li>
                <li>1/2 cup vinegar</li>
                <li>1/2 cup brown sugar</li>
                <li>4 cloves garlic, crushed</li>
                <li>1 onion, chopped</li>
                <li>3 pieces dried bay leaves</li>
                <li>1 teaspoon whole peppercorns</li>
                <li>1/2 cup water</li>
                <li>Salt to taste</li>
                <li>3 tablespoons cooking oil</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>In a pot, heat cooking oil. Saut√© garlic and onion until fragrant.</li>
                <li>Add pork belly and cook until lightly browned.</li>
                <li>Pour pineapple juice, soy sauce, and vinegar. Add brown sugar, dried bay leaves, whole peppercorns, and water. Stir to combine.</li>
                <li>Bring to a boil then lower the heat. Cover and simmer for about 1.5 to 2 hours or until the pork is tender. Add water as needed.</li>
                <li>Once the pork is tender and the sauce is reduced, adjust the seasoning with salt.</li>
                <li>Serve hot with steamed rice.</li>
            </ol>
