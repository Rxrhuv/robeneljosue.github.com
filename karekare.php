            <h2>Kare-Kare</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>1 kg oxtail, cut into pieces</li>
                <li>1/2 kg beef tripe, cleaned and boiled until tender</li>
                <li>2 cups peanut butter</li>
                <li>1 banana heart, sliced</li>
                <li>1 bundle string beans (sitaw), cut into 2-inch pieces</li>
                <li>1 bundle pechay (Chinese cabbage), sliced</li>
                <li>1 eggplant, sliced</li>
                <li>1/4 cup annatto seeds (achuete)</li>
                <li>4 cups rice wash (water used for washing rice)</li>
                <li>4 cloves garlic, minced</li>
                <li>1 onion, chopped</li>
                <li>3 tablespoons cooking oil</li>
                <li>Salt and pepper to taste</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>In a pan, heat cooking oil. Saut√© garlic and onion until fragrant.</li>
                <li>Add oxtail and beef tripe. Cook until lightly browned.</li>
                <li>Add rice wash and annatto seeds. Simmer until the meat is tender.</li>
                <li>Stir in peanut butter until well combined.</li>
                <li>Add banana heart, string beans, eggplant, and pechay. Cook until the vegetables are tender.</li>
                <li>Season with salt and pepper to taste.</li>
                <li>Serve hot with bagoong (shrimp paste) on the side and steamed rice.</li>
            </ol>
