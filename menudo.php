            <h2>Menudo</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>500g pork, diced</li>
                <li>2 medium-sized potatoes, diced</li>
                <li>1 medium-sized carrot, diced</li>
                <li>1 red bell pepper, diced</li>
                <li>1 green bell pepper, diced</li>
                <li>1 onion, chopped</li>
                <li>3 cloves garlic, minced</li>
                <li>1 cup tomato sauce</li>
                <li>1/4 cup liver spread</li>
                <li>1/4 cup soy sauce</li>
                <li>1/4 cup vinegar</li>
                <li>1 cup water</li>
                <li>Salt and pepper to taste</li>
                <li>2 tablespoons cooking oil</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>Heat cooking oil in a pan. Saut√© garlic and onion until fragrant.</li>
                <li>Add diced pork. Cook until browned.</li>
                <li>Pour soy sauce and vinegar. Let it simmer for 5 minutes.</li>
                <li>Add tomato sauce and liver spread. Stir until well combined.</li>
                <li>Pour water. Cover and simmer until pork is tender.</li>
                <li>Add diced potatoes and carrots. Cook until vegetables are tender.</li>
                <li>Add diced bell peppers. Cook for an additional 3-5 minutes.</li>
                <li>Season with salt and pepper to taste.</li>
                <li>Serve hot with steamed rice.</li>
            </ol>
