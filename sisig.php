            <h2>Sisig</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>500g pig's ears (or pork belly)</li>
                <li>1/2 cup vinegar</li>
                <li>1/4 cup soy sauce</li>
                <li>1 onion, chopped</li>
                <li>3 cloves garlic, minced</li>
                <li>2-3 green chilies, chopped</li>
                <li>1/4 cup calamansi juice (or lemon)</li>
                <li>Salt and pepper to taste</li>
                <li>2 tablespoons cooking oil</li>
                <li>2 tablespoons mayonnaise (optional)</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>Boil pig's ears in water until tender. Drain and let cool.</li>
                <li>Grill or broil the pig's ears until crispy. Chop into small pieces.</li>
                <li>In a pan, heat cooking oil. Saut√© garlic and onions until fragrant.</li>
                <li>Add chopped pig's ears and chilies. Cook for a few minutes.</li>
                <li>Add vinegar, soy sauce, and calamansi juice. Stir well and cook for another 5 minutes.</li>
                <li>Season with salt and pepper to taste. Stir in mayonnaise if desired.</li>
                <li>Serve hot and enjoy your Sisig!</li>
            </ol>
