            <h2>Sinigang na Baboy</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>1 kg pork ribs or belly</li>
                <li>1 packet sinigang mix</li>
                <li>1 onion, quartered</li>
                <li>2 tomatoes, quartered</li>
                <li>2 radishes, sliced</li>
                <li>1 eggplant, sliced</li>
                <li>2 cups kangkong leaves</li>
                <li>6 cups water</li>
                <li>Salt to taste</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>Boil pork in water with onion and tomatoes until tender.</li>
                <li>Add sinigang mix and vegetables. Simmer until vegetables are cooked.</li>
                <li>Season with salt.</li>
            </ol>
