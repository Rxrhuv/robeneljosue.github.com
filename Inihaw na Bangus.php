            <h2>Inihaw na Bangus</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>1 whole bangus (milkfish), scaled, gutted, and cleaned</li>
                <li>1/4 cup soy sauce</li>
                <li>1/4 cup calamansi juice (or lemon juice)</li>
                <li>3 cloves garlic, minced</li>
                <li>1 teaspoon ground black pepper</li>
                <li>Salt to taste</li>
                <li>Cubed cheese</li>
                <li>2 onions</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>Make diagonal slashes on both sides of the bangus to allow the marinade to penetrate.</li>
                <li>In a bowl, combine soy sauce, calamansi juice, minced garlic, ground black pepper, and salt.</li>
                <li>Marinate the bangus in the mixture for at least 1 hour, turning occasionally to ensure even marination.</li>
                <li>Preheat the grill to medium heat. Make sure the grill grates are clean and lightly oiled.</li>
                <li>Place the marinated bangus on the grill, skin side down. Grill for about 5-7 minutes on each side, or until the fish is cooked through and nicely charred.</li>
                <li>Serve hot with steamed rice and your favorite dipping sauce.</li>
            </ol>
