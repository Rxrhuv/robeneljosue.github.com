            <h2>Kaldereta</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>500g beef stew meat, cubed</li>
                <li>2 large potatoes, peeled and cubed</li>
                <li>1 large carrot, peeled and cubed</li>
                <li>1 red bell pepper, sliced</li>
                <li>1 green bell pepper, sliced</li>
                <li>1 onion, chopped</li>
                <li>3 cloves garlic, minced</li>
                <li>1 cup tomato sauce</li>
                <li>1/4 cup liver spread</li>
                <li>1/4 cup soy sauce</li>
                <li>1/4 cup vinegar</li>
                <li>1 cup beef broth</li>
                <li>2 tablespoons cooking oil</li>
                <li>Salt and pepper to taste</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>In a pot, heat cooking oil. Saut√© garlic and onion until fragrant.</li>
                <li>Add beef stew meat. Cook until browned on all sides.</li>
                <li>Pour soy sauce and vinegar. Let it simmer for 5 minutes.</li>
                <li>Add tomato sauce, liver spread, and beef broth. Stir until well combined.</li>
                <li>Cover and simmer for 1 to 1.5 hours or until the beef is tender.</li>
                <li>Add potatoes, carrots, and bell peppers. Cook until vegetables are tender.</li>
                <li>Season with salt and pepper to taste.</li>
                <li>Serve hot with steamed rice.</li>
            </ol>
