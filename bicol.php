            <h2>Bicol Express</h2>
            <p><strong>Ingredients:</strong></p>
            <ol class="recipe-list">
                <li>500g pork belly, thinly sliced</li>
                <li>2 cups coconut milk</li>
                <li>1 cup long green chili peppers (siling pansigang), chopped</li>
                <li>3 cloves garlic, minced</li>
                <li>1 onion, chopped</li>
                <li>2 tablespoons shrimp paste (bagoong alamang)</li>
                <li>1 tablespoon cooking oil</li>
                <li>Salt and pepper to taste</li>
            </ol><br>
            <p><strong>Instructions:</strong></p>
            <ul class="recipe-list">
                <li>In a pan, heat cooking oil. Saut√© garlic and onion until fragrant.</li>
                <li>Add pork belly and cook until lightly browned.</li>
                <li>Add shrimp paste and stir for about 2 minutes.</li>
                <li>Pour coconut milk and bring to a simmer. Let it cook for about 15 minutes or until the pork is tender.</li>
                <li>Add chopped long green chili peppers. Cook for another 5 minutes.</li>
                <li>Season with salt and pepper to taste.</li>
                <li>Serve hot with steamed rice.</li>
            </ul>
