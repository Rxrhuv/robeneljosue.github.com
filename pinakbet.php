            <h2>Pinakbet</h2>
            <p><strong>Ingredients:</strong></p>
            <ul class="recipe-list">
                <li>1 medium-sized bitter gourd (ampalaya), sliced</li>
                <li>1 medium-sized eggplant, sliced</li>
                <li>1 medium-sized squash (kalabasa), cubed</li>
                <li>8-10 pieces okra</li>
                <li>1 cup string beans (sitaw), cut into 2-inch pieces</li>
                <li>1 medium-sized tomato, chopped</li>
                <li>1 medium-sized onion, chopped</li>
                <li>4 cloves garlic, minced</li>
                <li>1/4 cup shrimp paste (bagoong alamang)</li>
                <li>2 cups water</li>
                <li>2 tablespoons cooking oil</li>
                <li>Salt and pepper to taste</li>
            </ul><br>
            <p><strong>Instructions:</strong></p>
            <ol class="recipe-list">
                <li>In a pan, heat cooking oil. Saut√© garlic, onion, and tomato until fragrant.</li>
                <li>Add shrimp paste and stir for about 2 minutes.</li>
                <li>Add bitter gourd, eggplant, squash, okra, and string beans. Cook for about 5 minutes.</li>
                <li>Pour water and bring to a simmer. Let it cook for about 15 minutes or until the vegetables are tender.</li>
                <li>Season with salt and pepper to taste.</li>
                <li>Serve hot with steamed rice.</li>
            </ol>
